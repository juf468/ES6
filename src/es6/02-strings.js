//DECLARO LAS VARIABLES

let hello = 'Hello';
let world = 'World';

// CONCATENO CON '+'
let epicPhrase = hello + ' ' + world + '!';

//TEMPLATE LITERALS
let epicPhrase2 = `${hello} ${world}!`;
console.log('epicPhrase2 :>> ', epicPhrase2);

//Multi-line strings
let lorem = 'esto es un string \n' + 'esto es otra linea' //utilizamos \n como salto de linea
let lorem2 = `esta es una frase epica
la continuacion de esta frase epica` //utilizamos 'enter' como salto de linea y `` para concatenar

console.log('lorem :>> ', lorem);
console.log('lorem2 :>> ', lorem2);
